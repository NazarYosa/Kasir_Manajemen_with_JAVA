    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    int i = jTable1.getSelectedRow();
    if (i == -1) {
        return;
    }
    
    String id = (String) model.getValueAt(i, 0);
    
    int pernyataan = JOptionPane.showConfirmDialog(null, "Yakin Data Akan Dihapus","Konfirmasi", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
    if (pernyataan == JOptionPane.OK_OPTION) {
        Connection c = null;
        try {
            c = koneksi.getKoneksi();
            // Nonaktifkan autocommit agar dapat melakukan rollback jika terjadi kesalahan
            c.setAutoCommit(false);
            
            // Hapus terlebih dahulu data terkait dari tabel 'penjualanrinci'
            String sqlDeletePenjualanRinci = "DELETE FROM penjualanrinci WHERE ID_Barang = ?";
            PreparedStatement pDeletePenjualanRinci = c.prepareStatement(sqlDeletePenjualanRinci);
            pDeletePenjualanRinci.setString(1, id);
            pDeletePenjualanRinci.executeUpdate();
            pDeletePenjualanRinci.close();
            
            // Hapus baris dari tabel 'barang'
            String sqlDeleteBarang = "DELETE FROM barang WHERE ID_Barang = ?";
            PreparedStatement pDeleteBarang = c.prepareStatement(sqlDeleteBarang);
            pDeleteBarang.setString(1, id);
            pDeleteBarang.executeUpdate();
            pDeleteBarang.close();
            
            // Commit transaksi jika tidak terjadi kesalahan
            c.commit();
            
            JOptionPane.showMessageDialog(null, "Data Terhapus");
            // Bersihkan input setelah data terhapus
            clear();
        } catch (Exception e) {
            // Rollback transaksi jika terjadi kesalahan
            if (c != null) {
                try {
                    c.rollback();
                } catch (SQLException ex) {
                    System.out.println("Terjadi Kesalahan saat melakukan rollback: " + ex);
                }
            }
            
            System.out.println("Terjadi Kesalahan" + e);
        } finally {
            if (c != null) {
                try {
                    // Set autocommit ke true kembali
                    c.setAutoCommit(true);
                } catch (SQLException ex) {
                    System.out.println("Terjadi Kesalahan saat menutup koneksi: " + ex);
                }
            }
            
            btnTambah.setEnabled(true);
            btnSimpan.setEnabled(false);
            btnHapus.setEnabled(false);
            btnBatal.setEnabled(false);
            loadData();
            autonumber();
        }
    }
    if (pernyataan == JOptionPane.CANCEL_OPTION) {
        // Tindakan yang akan diambil jika pengguna membatalkan penghapusan
    }
    }//GEN-LAST:event_btnHapusActionPerformed

