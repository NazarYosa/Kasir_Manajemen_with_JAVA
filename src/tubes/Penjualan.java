    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        String noTransaksi = txNoTransaksi.getText();
        String tanggal = txTanggal.getText();
        String idCustomer = txIDCustomer.getText();
        String total = txTotalBayar.getText();

        try {
            Connection c = koneksi.getKoneksi();
            String sql = "INSERT INTO penjualan VALUES (?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, noTransaksi);
            p.setString(2, tanggal);
            p.setString(3, idCustomer);
            p.setString(4, total);
            p.executeUpdate();
            p.close();
        } catch (Exception e) {
            System.out.println("simpan penjualan error");
        }

        // buat nambahin ke database penjualan rinci
        try {
            Connection c = koneksi.getKoneksi();
            int baris = jTable1.getRowCount();

            for (int i = 0; i < baris; i++) {
                String sql = "INSERT INTO penjualanrinci(NoFaktur, ID_Barang, Nama_Barang, Jumlah, Harga, Total) VALUES('"
                + jTable1.getValueAt(i, 0) +"','" // no faktur
                + jTable1.getValueAt(i, 1) +"','" // id barang
                + jTable1.getValueAt(i, 2) +"','" // nama barang
                + jTable1.getValueAt(i, 3) +"','" // jumlah
                + jTable1.getValueAt(i, 4) +"','" // harga
                + jTable1.getValueAt(i, 5) // total
                +"')";
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
            }
        } catch (Exception e) {
            System.out.println("simpan penjualanrinci error");
        }

        // buat update stok di tabel barang
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            int baris = jTable1.getRowCount();

            for (int i = 0; i < baris; i++)
            {
                String sql_stok = "UPDATE barang "
                + "set Stok = Stok - " + jTable1.getValueAt(i, 3) + " "
                + "where ID_Barang = " +"\"" + jTable1.getValueAt(i, 1) + "\"";
                System.out.println(sql_stok);
                s.executeUpdate(sql_stok);
            }
            s.close();
        } catch (Exception e) {
            System.out.println("update stok error" + e);
        }
        clear();
        utama();
        kosong();
        txTampil.setText("Rp. 0");
        autonumber();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void txBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txBayarActionPerformed
        // TODO add your handling code here:
        int totalBayar, bayar, kembalian;

        totalBayar = Integer.valueOf(txTotalBayar.getText());
        bayar = Integer.valueOf(txBayar.getText());

        if (totalBayar > bayar) {
            JOptionPane.showMessageDialog(null, "Uang tidak cukup untuk melakukan pembayaran");
        } else {
            kembalian = bayar - totalBayar;
            txKembalian.setText(String.valueOf(kembalian));
        }
    }//GEN-LAST:event_txBayarActionPerformed

